#include <iostream>

// Función para convertir de kilómetros a millas
double kmToMiles(double km) {
    return km * 0.621371;
}

// Función para convertir de millas a kilómetros
double milesToKm(double miles) {
    return miles * 1.60934;
}

// Función para convertir de metros a pulgadas
double metersToInches(double meters) {
    return meters * 39.3701;
}

// Función para convertir de pulgadas a metros
double inchesToMeters(double inches) {
    return inches * 0.0254;
}

// Función para convertir de libras a kilogramos
double poundsToKg(double pounds) {
    return pounds * 0.453592;
}

// Función para convertir de kilogramos a libras
double kgToPounds(double kg) {
    return kg * 2.20462;
}

    
    
           
         
    

    
